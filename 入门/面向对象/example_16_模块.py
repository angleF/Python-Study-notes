# 包(Package)
# 包也是一个模块
# 当一个模块中的代码过多或者一个陌路爱需要被分解时，这时候就需要用到包
# 普通的模块就是一个py文件，而包是一个文件夹
#   包中必须要一个名为__init__.py的文件，这个文件中可以包含包中的主要内容
# 引入包语法：
# from 报名 import 包中的文件名或者__init__.py中的变量和函数
from test_package import test_m as t
t.fn(2,4)
